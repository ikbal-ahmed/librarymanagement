    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        try {
            fileWrite=new FileWriter(connectionFile);
            fileWrite.write(hostField.getText() + System.getProperty("line.separator"));
            fileWrite.write(portField.getText() + System.getProperty("line.separator"));
            fileWrite.write(databaseFiled.getText() + System.getProperty("line.separator"));
            fileWrite.write(userNameField.getText() + System.getProperty("line.separator"));
            fileWrite.write(passField.getText() + System.getProperty("line.separator"));
            fileWrite.close();
            super.dispose();
            new loginFrame().setVisible(true);
        } catch (IOException ex) {
            System.out.println(ex);   
        }
    }//GEN-LAST:event_jButton3ActionPerformed

